<script>
function toggleMobileMenu() {
    const mobileMenu = document.getElementById('mobile-menu');
    const menuIcon = document.getElementById('menu-icon');
    const closeIcon = document.getElementById('close-icon');
    const button = document.getElementById('mobile-menu-button');
    
    const isHidden = mobileMenu.classList.contains('hidden');
    
    if (isHidden) {
        mobileMenu.classList.remove('hidden');
        menuIcon.classList.add('hidden');
        closeIcon.classList.remove('hidden');
        button.setAttribute('aria-expanded', 'true');
    } else {
        mobileMenu.classList.add('hidden');
        menuIcon.classList.remove('hidden');
        closeIcon.classList.add('hidden');
        button.setAttribute('aria-expanded', 'false');
        
        // Cerrar submenú de programas si está abierto
        const programsMenu = document.getElementById('mobile-programs-menu');
        const programsIcon = document.getElementById('mobile-programs-icon');
        if (programsMenu && !programsMenu.classList.contains('hidden')) {
            programsMenu.classList.add('hidden');
            programsIcon.classList.remove('rotate-180');
        }
    }
}

function toggleMobileProgramas() {
    const programsMenu = document.getElementById('mobile-programs-menu');
    const programsIcon = document.getElementById('mobile-programs-icon');
    
    programsMenu.classList.toggle('hidden');
    programsIcon.classList.toggle('rotate-180');
}

// Cerrar menú móvil al redimensionar ventana
window.addEventListener('resize', function() {
    if (window.innerWidth >= 1024) { // lg breakpoint
        const mobileMenu = document.getElementById('mobile-menu');
        const menuIcon = document.getElementById('menu-icon');
        const closeIcon = document.getElementById('close-icon');
        const button = document.getElementById('mobile-menu-button');
        
        if (mobileMenu && !mobileMenu.classList.contains('hidden')) {
            mobileMenu.classList.add('hidden');
            menuIcon.classList.remove('hidden');
            closeIcon.classList.add('hidden');
            button.setAttribute('aria-expanded', 'false');
        }
    }
});

// Smooth scroll para enlaces ancla
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

